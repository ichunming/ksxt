package com.ksxt.common.constant;

public enum MessageType {
	SUCC("SUCC", "alert-success", "成功通知"),
	INFO("INFO", "alert-info", "一般通知"),
	WARN("WARN", "alert-warning", "警告"),
	ERROR("ERR", "alert-danger", "危险错误");

	private String code;
	private String clazz;
	private String desc;
	
	private MessageType(String code, String clazz, String desc) {
		this.code = code;
		this.clazz = clazz;
		this.desc = desc;
	}
	
	public String getCode() {
		return code;
	}
	public String getClazz() {
		return clazz;
	}
	public String getDesc() {
		return desc;
	}
}
