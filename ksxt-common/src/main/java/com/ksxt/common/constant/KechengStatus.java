package com.ksxt.common.constant;

public enum KechengStatus {

	INVALID(0, "无效"),
	VALID(1, "有效");
	
	private int code;
	private String desc;
	
	private KechengStatus(int code, String desc) {
		this.code = code;
		this.desc = desc;
	}
	
	public int getCode() {
		return code;
	}

	public String toString() {
		return desc;
	}
}
