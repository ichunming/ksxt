package com.ksxt.common.constant;

public enum UserRole {
	USER(0, "common user"),
	ADMIN(1, "admin user");
	
	private int code;
	private String desc;
	
	private UserRole(int code, String desc) {
		this.code = code;
		this.desc = desc;
	}
	
	public int getCode() {
		return code;
	}
	
	public String toString() {
		return desc;
	}
}
